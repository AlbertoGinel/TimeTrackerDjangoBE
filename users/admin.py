from django.contrib import admin
from django.contrib.auth.admin import UserAdmin
from .models import CustomUser

class CustomUserAdmin(UserAdmin):
    list_display = ('username', 'email', 'userTimezone', 'is_staff', 'date_joined', 'is_superuser')
    
    # Add userTimezone to both add and change forms
    fieldsets = (
        (None, {'fields': ('username', 'password')}),
        ('Personal info', {'fields': ('first_name', 'last_name', 'email')}),
        ('Permissions', {
            'fields': ('is_active', 'is_staff', 'is_superuser', 'groups', 'user_permissions'),
        }),
        ('Important dates', {'fields': ('last_login', 'date_joined')}),
        ('Timezone', {'fields': ('userTimezone',)}),  # Fixed: removed trailing space
    )
    
    # For add user form
    add_fieldsets = (
        (None, {
            'classes': ('wide',),
            'fields': ('username', 'email', 'password1', 'password2', 'is_staff', 'userTimezone'),
        }),
    )

admin.site.register(CustomUser, CustomUserAdmin)

